#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	char			*s;
	unsigned int	i;

	s = (char*)b;
	i = 0;
	while (i < len)
	{
		*(s + i) = c;
		i++;
	}
	return (b);
}
