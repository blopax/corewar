#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	j;
	char	*mem;

	mem = s1;
	j = 0;
	while (*s1)
		s1++;
	while (*s2 && j < n)
	{
		*s1 = *s2;
		s1++;
		s2++;
		j++;
	}
	*s1 = '\0';
	return (mem);
}
